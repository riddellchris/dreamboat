PHONE APPOINTMENT GUIDELINES
Requirements
A user should either concurrently use the Dreamboat platform or use a pen and paper.
Start time
The scheduled time should be such that a user is not rushed but they should also complete any emails and remove any other distractions.
In order to achieve maximum efficiency for both Pilots and users we always call on the start time exactly. 
Confirmation
All appointment times are confirmed by a digital calendar invitation to the user such that they may remember the upcoming appointment as required and ensure they work around it where possible and plausible.
Adjustment
Where you need to adjust appointment times we will try to accommodate you as much as possible within reason, however sometimes issues arise. 
